public void Verificarsem(){
    try{
        
        if(MesAltera != MesFixo-1 & DiaClik !=0){
        DiaAltera =1;
        }
        if(AnoAltera != AnoFixo-1 & DiaClik !=0){
        DiaAltera =1;
        }
        
        DiaClik =1;
        
        
        if(DiaAltera == DiaFixo & MesAltera+1 ==MesFixo & AnoAltera == AnoFixo ){
        diapag1.setForeground(Color.red);
        diapag2.setForeground(Color.red);
        }else{
        diapag1.setForeground(Color.BLACK);
        diapag2.setForeground(Color.BLACK);
        }
        
        
    MesAltera = Smes.getSelectedIndex();
    AnoAltera = Integer.parseInt(Sano.getSelectedItem().toString());
    Calendar calen = Calendar.getInstance();
    calen.set(AnoAltera, MesAltera, DiaAltera);
    
    diapag1.setText("" + DiaAltera);
    diapag2.setText("" + DiaAltera);
    
    int semanaN = calen.get(Calendar.DAY_OF_WEEK);
    if(semanaN == Calendar.SUNDAY){
    datapag1.setText("Domingo");
    datapag2.setText("Domingo");
    }else if (semanaN == Calendar.MONDAY){
    datapag1.setText("Segunda");
    datapag2.setText("Segunda");
    
    }else if (semanaN == Calendar.TUESDAY){
    datapag1.setText("Ter√ßa");
    datapag2.setText("Ter√ßa");
    
    }else if (semanaN == Calendar.WEDNESDAY){
    datapag1.setText("Quarta");
    datapag2.setText("Quarta");
    
    }else if (semanaN == Calendar.THURSDAY){
    datapag1.setText("Quinta");
    datapag2.setText("Quinta");
    
    }else if (semanaN == Calendar.FRIDAY){
    datapag1.setText("Sexta");
    datapag2.setText("Sexta");
    
    }else if (semanaN == Calendar.SATURDAY){
    datapag1.setText("S√°bado");
    datapag2.setText("S√°bado");
    
    }
    }catch (Exception e){
    
        JOptionPane.showMessageDialog(null,"Error");
    }

}

